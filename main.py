"""
Задача 30:  
Заполните массив элементами арифметической прогрессии. Её первый элемент, разность и количество элементов 
нужно ввести с клавиатуры. Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
Каждое число вводится с новой строки.
"""

# 1 Вариант

a1 = int(input('Введите первый элемент арифметической прогрессии: '))
d = int(input('Введите разность между ее членами: '))
n = int(input('Введите количество членов прогрессии: '))

for i in range(n):
    print(a1 + i * d, end=' ')
print('\n')


