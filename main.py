# 3 Вариант (через рекурсию)

a = int(input('Введите первый элемент арифметической прогрессии: '))
d = int(input('Введите разность между ее членами: '))
n = int(input('Введите количество членов прогрессии: '))

def rec(n):
    if n == 0:
        return 0
    return (a + d*(n-1))

list = []              
for i in range(1, n+1):
    list.append(rec(i))
print(*list)  



